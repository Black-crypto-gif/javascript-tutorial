// Arithmetic operators
let x = 10;
let y = 5;

console.log(x + y); // 15
console.log(x - y); // 5
console.log(x * y); // 50
console.log(x / y); // 2
console.log(x % y); // 0
console.log(x ** y); // 100000



// Assignment operators
let z = 5;
z += 2; // equivalent to z = z + 2
console.log(z); // 7

z -= 2; // equivalent to z = z - 2
console.log(z); // 5

z *= 2; // equivalent to z = z * 2
console.log(z); // 10

z /= 2; // equivalent to z = z / 2
console.log(z); // 5

z %= 2; // equivalent to z = z % 2
console.log(z); // 1



// Comparison operators
console.log(x > y);  // true
console.log(x < y);  // false
console.log(x >= y); // true
console.log(x <= y); // false
console.log(x === y); // false
console.log(x !== y); // true



// Logical operators
console.log(x > 5 && y < 10); // true
console.log(x > 5 || y > 10); // true
console.log(!(x > 5));       // false
